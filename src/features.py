import pandas as pd
import numpy as np

def add_technical_features_from_csv(path):
    df = pd.read_csv(path)
    return df

def add_lag_features(df, target_col='GiaDongCua', lags=[1]):
    for lag in lags:
        df[f'{target_col}_lag{lag}'] = df[target_col].shift(lag)
    return df.dropna().reset_index(drop=True)